    private void jbNovoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbNovoActionPerformed
        TabelaController controle = new TabelaController();
        id = controle.ultimoIdJogos() + 1;
        limparCampos();
        jtxCodigo.setText(String.valueOf(id));
        jbNovo.setEnabled(false);
        jbSalvar.setEnabled(true);
        jbPesquisar.setEnabled(false);
        jtxCodigo.setEditable(false);
    }//GEN-LAST:event_jbNovoActionPerformed

    private void jbSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbSalvarActionPerformed

        if(!jtxPlacar.getText().trim().equals("")){
            TabelaModel jogo = new TabelaModel();
            jogo.setIdJogo(Integer.parseInt(jtxCodigo.getText()));
            jogo.setPlacar(Integer.parseInt(jtxPlacar.getText()));
            
            TabelaController controle = new TabelaController();
            if(controle.InserirJogos(jogo)){
                JOptionPane.showMessageDialog(this, "Jogo Inserido com sucesso!");
                edicaoComponentes();
                limparCampos();
                atualizarTabela();
                jbNovo.setEnabled(true);
                jbPesquisar.setEnabled(true);
                jtxCodigo.setEditable(true);
            }else{
                JOptionPane.showMessageDialog(this, "Erro ao Inserir jogo!",
                    "Retorno", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_jbSalvarActionPerformed

    private void jbEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbEditarActionPerformed
        TabelaModel jogo = new TabelaModel();
        jogo.setIdJogo(Integer.parseInt(jtxCodigo.getText()));
        TabelaController controle = new TabelaController();
        if(controle.editarJogos(jogo)){
            JOptionPane.showMessageDialog(this, "Jogo Editado com sucesso!");
            limparCampos();
            edicaoComponentes();
            atualizarTabela();
        }else{
            JOptionPane.showMessageDialog(this, "Erro ao Editar jogo!",
                "Retorno", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jbEditarActionPerformed

    private void jbExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbExcluirActionPerformed
        TabelaModel jogo = new TabelaModel();
        TabelaController controle = new TabelaController();
        jtxCodigo.setText(String.valueOf(controle.ultimoIdJogos()));
        jogo.setIdJogo(Integer.parseInt(jtxCodigo.getText()));
        
        if(controle.excluirJogo(jogo)){
            JOptionPane.showMessageDialog(this, "Jogo Excluido com sucesso!");
            limparCampos();
            edicaoComponentes();
            atualizarTabela();
        }else{
            JOptionPane.showMessageDialog(this, "Erro ao Excluir jogo!",
                "Retorno", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jbExcluirActionPerformed

